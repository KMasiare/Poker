package poker;

public class Result {
	Score score;
	int highestCard;
	
	public Score getScore() {
		return score;
	}
	public void setScore(Score score) {
		this.score = score;
	}
	public int getHighestCard() {
		return highestCard;
	}
	public void setHighestCard(int highestCard) {
		this.highestCard = highestCard;
	}
	
}
